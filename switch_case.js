//switch case
//Create a result sheet..
let colors = 'red';
switch ( colors )
{
    case 'blue':
        console.log( 'Color is blue' );
        break;
    case 'red':
        console.log( 'Color is red' );
        break;
    case 'yellow':
        console.log( 'color is yellow' );
        break;
    default:
        console.log('color is none');
}